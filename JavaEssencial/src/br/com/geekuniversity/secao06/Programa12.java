package br.com.geekuniversity.secao06;//Tipos de dados
//Numéricos(Inteiros e Reais)

public class Programa12 {
    public static void main(String[] args) {
        //Tipos Primitivos/Primários
        float preco = 23.4f; //Por padrão, os números reais são considerados double, por isso o f
        double preco1 = 23.4;//Não precisa do f, mas ocupa mais espaço em memória, ideal para valores reais

        //Tipos Primários/primitivos
        Float preco3 = 44.5f;
        Double preco4 = 44.5;

        System.out.println("n1 = " + preco);
        System.out.println("n2 = " + preco1);
        System.out.println("n3 = " + preco3);
        System.out.println("n4 = " + preco4);


    }

}
